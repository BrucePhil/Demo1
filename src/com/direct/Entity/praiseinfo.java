package com.direct.Entity;

public class praiseinfo {
	private int praiseid;//点赞id
	private int praisenum;//点赞数量
	private int praid;//点赞人userid
	private int praisetitleid;//点赞文章titleid
	public int getPraiseid() {
		return praiseid;
	}
	public void setPraiseid(int praiseid) {
		this.praiseid = praiseid;
	}
	public int getPraisenum() {
		return praisenum;
	}
	public void setPraisenum(int praisenum) {
		this.praisenum = praisenum;
	}
	public int getPraid() {
		return praid;
	}
	public void setPraid(int praid) {
		this.praid = praid;
	}
	public int getPraisetitleid() {
		return praisetitleid;
	}
	public void setPraisetitleid(int praisetitleid) {
		this.praisetitleid = praisetitleid;
	}

}
